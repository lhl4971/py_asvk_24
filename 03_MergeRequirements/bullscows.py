# Импортируем модуль random для генерации случайных слов
import random

# Определяем функцию bullscows, которая принимает два слова guess и secret
# и возвращает количество быков и коров в guess по отношению к secret
def bullscows(guess, secret):
    # Инициализируем счетчики быков и коров
    bulls = 0
    cows = 0
    # Проходим по каждой букве в guess
    for i in range(len(guess)):
        # Если буква совпадает с буквой в secret на том же месте, увеличиваем быков
        if guess[i] == secret[i]:
            bulls += 1
        # Иначе, если буква есть в secret, но на другом месте, увеличиваем коров
        elif guess[i] in secret:
            cows += 1
    # Возвращаем кортеж из быков и коров
    return (bulls, cows)

# Определяем функцию gameplay, которая принимает три аргумента:
# ask - функция, которая спрашивает у пользователя слово
# inform - функция, которая выводит пользователю результат
# words - список слов, из которых выбирается загаданное слово
def gameplay(ask, inform, words):
    # Выбираем случайное слово из words и сохраняем его в secret
    secret = random.choice(words)
    # Инициализируем счетчик попыток
    attempts = 0
    # Запускаем бесконечный цикл
    while True:
        # Увеличиваем счетчик попыток
        attempts += 1
        # Спрашиваем у пользователя слово с помощью функции ask
        guess = ask("Введите слово: ", words)
        # Вычисляем количество быков и коров с помощью функции bullscows
        bulls, cows = bullscows(guess, secret)
        # Выводим пользователю результат с помощью функции inform
        inform("Быки: {}, Коровы: {}", bulls, cows)
        # Если количество быков равно длине слова, значит слово отгадано
        if bulls == len(secret):
            # Возвращаем количество попыток
            return attempts

